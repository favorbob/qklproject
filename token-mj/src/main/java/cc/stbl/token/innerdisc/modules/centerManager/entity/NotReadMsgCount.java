package cc.stbl.token.innerdisc.modules.centerManager.entity;

/**
 * 系统公告未读总数、简介未读总数
 */
public class NotReadMsgCount {

    private String sysMsgCount;

    private String introMsgCount;

    public String getSysMsgCount() {
        return sysMsgCount;
    }

    public void setSysMsgCount(String sysMsgCount) {
        this.sysMsgCount = sysMsgCount;
    }

    public String getIntroMsgCount() {
        return introMsgCount;
    }

    public void setIntroMsgCount(String introMsgCount) {
        this.introMsgCount = introMsgCount;
    }
}
